class prime_sieve {
    void build(int n, vector<int> &isprime) {
        isprime.resize(n + 1);
        isprime[2] = 1;
        for (int i = 3; i <= n; i++) {
            isprime[i] = i % 2;
        };
        for (int i = 3; i * i <= n; i += 2) {
            if (isprime[i]) {
                for (int j = i * i; j <= n; j += i) {
                    isprime[j] = 0;
                };
            };
        };
    };
    /////////////////
    public:
    vector<int> isprime;
    prime_sieve(int n = 1e6) {
        build(n, isprime);
    };
};
