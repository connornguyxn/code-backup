    for (int i = 1; i <= 99999; i++) {
        int cur = i, len = 0, sum = 0;
        while (cur > 0) {
            len++;
            sum += cur % 10;
            cur /= 10;
        }
        cnt[len][0] += isprime(sum);
    };
    
    cnt[1][1] = 0;
